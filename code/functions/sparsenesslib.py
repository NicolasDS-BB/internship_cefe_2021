def treves_rolls(vector):
    """
    compute modified treve-rolls population sparsennes, formula from (wilmore et all, 2000)
    """
    denominator = 0
    numerator = 0
    length = len(vector)
    for each in vector:
        numerator += abs(each)
        denominator += (each*each)/length 
    tr=1 - (((numerator/length)*(numerator/length)) /denominator)
    return tr 
